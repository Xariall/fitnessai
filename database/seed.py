async def seed() -> None:
    await init_db()
    async with AsyncSessionLocal() as session:
        # Exercises — insert only if table is empty
        ex_count = await session.scalar(select(Exercise).with_only_columns(Exercise.id).limit(1))
        if ex_count is None:
            session.add_all([
                Exercise(name=n, muscle_group=mg, equipment=eq, description=desc)
                for n, mg, eq, desc in EXERCISES
            ])

        # Food products — additive: insert only missing names (safe to re-run)
        existing_result = await session.execute(select(FoodProduct.name))
        existing_names = {row[0].lower() for row in existing_result}

        new_foods = [
            FoodProduct(name=n, calories=cal, protein=prot, fat=fat, carbs=carb)
            for n, cal, prot, fat, carb in FOOD_PRODUCTS
            if n.lower() not in existing_names
        ]

        if new_foods:
            session.add_all(new_foods)

        await session.commit()

        total_foods = len(existing_names) + len(new_foods)
        print(
            f"Seed: {len(EXERCISES)} exercises | "
            f"food products: {total_foods} total "
            f"({len(new_foods)} newly added)"
        )


if __name__ == "__main__":
    asyncio.run(seed())
